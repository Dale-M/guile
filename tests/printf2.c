  jit_addi_i(JIT_R0, JIT_R0, 1);
  jit_movr_i(JIT_RET, JIT_R0);
  jit_ret();
       
  jit_flush_code(codeBuffer, jit_get_ip().ptr);
     
  /* call the generated code, passing 5 as an argument */
